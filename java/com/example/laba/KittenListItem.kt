import com.example.laba.data.DataProvider
import com.example.laba.data.model.Kitten
import com.example.laba.ui.theme.graySurface
import com.example.laba.ui.theme.typography

@Composable
fun KittenListItem(kitten: Kitten, navigateToProfile: (Kitten) -> Unit,) {
    Card(
        modifier = Modifier
            .padding(horizontal = 8.dp, vertical = 8.dp)
            .fillMaxWidth(),
        elevation = 2.dp,
        backgroundColor = graySurface,
        shape = RoundedCornerShape(corner = CornerSize(16.dp)),
    ) {
        Row(Modifier.clickable { navigateToProfile(kitten) }) {
            KittenImage(kitten)
            Column(
                modifier = Modifier
                    .padding(8.dp)
                    .fillMaxWidth()
                    .align(Alignment.CenterVertically)
            ) {
                Text(text = kitten.title, style = typography.h6)
                Text(text = kitten.sex, style = typography.caption)
            }
        }
    }
}

@Composable
private fun KittenImage(kitten: Kitten) {
    Image(
        painter = painterResource(id = kitten.kittenImageId),
        contentDescription = null,
        contentScale = ContentScale.Crop,
        modifier = Modifier
            .padding(8.dp)
            .size(84.dp)
            .clip(RoundedCornerShape(corner = CornerSize(16.dp)))
    )
}
@Preview
@Composable
fun PreviewKittenItem() {
    val kitten = DataProvider.kitten
    KittenListItem(kitten = kitten, navigateToProfile = {})
}
